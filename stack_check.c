#include "push_swap.h"

int	is_assending(t_ps *ps)
{
	t_stack *aa;

	aa = ps->stack.a;
	while (aa)
	{
		if (aa->n > aa->next->n)
			return (0);
		aa = aa->next;
	}
	return (1);
}
