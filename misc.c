#include "push_swap.h"

void	clean(t_ps **ps)
{
	ft_stackdel(&(*ps)->stack.a);
	(*ps)->stack.a = NULL;
	ft_stackdel(&(*ps)->stack.b);
	(*ps)->stack.b = NULL;
	free(*ps);
	*ps = NULL;
}
